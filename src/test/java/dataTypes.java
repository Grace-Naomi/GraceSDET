public class dataTypes {
    public static void main(String[] args){
        //Primitive & Non-Primitive Data Types

        //Non-Primitive : Class, Arrays, Strings & Interfaces.

        //Primitive : Integer, Decimal, Character & Boolean.

        //Integer
        // byte - Size is 2 power 8. -128 to -1 & 0 to 127 is the range.
        //short - Size is 2 power 16. - 32768 to -1 & 0 to 32767 is the range.
        //int - Size is 2 power 32.
        //long - Size is 2 power 64.

        //Decimals
        //float - Size is 2 power 32.
        //double - Size is 2 power 64.

        //Character
        //char - Size is 2 power 16.
        //Single quotes and single letter.

        //boolean - true or false

        byte a = 4;
        //dataType variableName = value;
        short b = 130;
        int c = 256799;
        long d = 4534546;
        System.out.println(a*40);
        float e = 1.23f;
        double f = 2.34645;
        char g = 'a';
        char h = 120;
        char i = '7';

        boolean j = false;

        System.out.println(e);
        System.out.println(e+5);
        System.out.println("e");
        System.out.println('e'+5);
        System.out.println('e'+'f');
        System.out.println("e"+5);
    }
}
